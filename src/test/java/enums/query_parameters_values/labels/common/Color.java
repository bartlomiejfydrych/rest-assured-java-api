package enums.query_parameters_values.labels.common;

import enums.query_parameters_values.interfaces.QueryParamValue;

public enum Color implements QueryParamValue {

    // ==========================================================================================================
    // ENUMS
    // ==========================================================================================================

    YELLOW("yellow"),
    PURPLE("purple"),
    BLUE("blue"),
    RED("red"),
    GREEN("green"),
    ORANGE("orange"),
    BLACK("black"),
    SKY("sky"),
    PINK("pink"),
    LIME("lime");

    // ==========================================================================================================
    // INTERNAL ENUM VALUE
    // ==========================================================================================================

    private final String value;

    // ==========================================================================================================
    // CONSTRUCTOR
    // ==========================================================================================================

    Color(String value) {
        this.value = value;
    }

    // ==========================================================================================================
    // QUERY PARAMETER VALUE ACCESSOR
    // ==========================================================================================================

    @Override
    public String value() {
        return value;
    }
}
