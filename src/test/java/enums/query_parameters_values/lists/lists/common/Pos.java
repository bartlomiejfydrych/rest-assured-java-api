package enums.query_parameters_values.lists.lists.common;

import enums.query_parameters_values.interfaces.QueryParamValue;

public enum Pos implements QueryParamValue {

    // ==========================================================================================================
    // ENUMS
    // ==========================================================================================================

    TOP("top"),
    BOTTOM("bottom");

    // ==========================================================================================================
    // INTERNAL ENUM VALUE
    // ==========================================================================================================

    private final String value;

    // ==========================================================================================================
    // CONSTRUCTOR
    // ==========================================================================================================

    Pos(String value) {
        this.value = value;
    }

    // ==========================================================================================================
    // QUERY PARAMETER VALUE ACCESSOR
    // ==========================================================================================================

    @Override
    public String value() {
        return value;
    }
}
