package enums.query_parameters_values.boards.boards.common;

import enums.query_parameters_values.interfaces.QueryParamValue;

public enum PrefsPermissionLevel implements QueryParamValue {

    // ==========================================================================================================
    // ENUMS
    // ==========================================================================================================

    ORG("org"),
    PRIVATE("private"),
    PUBLIC("public");

    // ==========================================================================================================
    // INTERNAL ENUM VALUE
    // ==========================================================================================================

    private final String value;

    // ==========================================================================================================
    // CONSTRUCTOR
    // ==========================================================================================================

    PrefsPermissionLevel(String value) {
        this.value = value;
    }

    // ==========================================================================================================
    // QUERY PARAMETER VALUE ACCESSOR
    // ==========================================================================================================

    @Override
    public String value() {
        return value;
    }
}
