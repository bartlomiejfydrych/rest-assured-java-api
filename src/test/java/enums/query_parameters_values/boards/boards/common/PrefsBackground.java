package enums.query_parameters_values.boards.boards.common;

import enums.query_parameters_values.interfaces.QueryParamValue;

public enum PrefsBackground implements QueryParamValue {

    // ==========================================================================================================
    // ENUMS
    // ==========================================================================================================

    BLUE("blue"),
    ORANGE("orange"),
    GREEN("green"),
    RED("red"),
    PURPLE("purple"),
    PINK("pink"),
    LIME("lime"),
    SKY("sky"),
    GREY("grey");

    // ==========================================================================================================
    // INTERNAL ENUM VALUE
    // ==========================================================================================================

    private final String value;

    // ==========================================================================================================
    // CONSTRUCTOR
    // ==========================================================================================================

    PrefsBackground(String value) {
        this.value = value;
    }

    // ==========================================================================================================
    // QUERY PARAMETER VALUE ACCESSOR
    // ==========================================================================================================

    @Override
    public String value() {
        return value;
    }
}
